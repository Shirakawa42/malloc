#include "malloc.h"

void	ft_bzero(void *s, size_t n)
{
	char	*u;
	size_t	i;

	if (!n)
		return ;
	i = 0;
	u = (char*)s;
	while (n > i)
	{
		u[n - 1] = '\0';
		n--;
	}
}
